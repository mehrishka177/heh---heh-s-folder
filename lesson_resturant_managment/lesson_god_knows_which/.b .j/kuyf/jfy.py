# Prompt the user to input a number
num = float(input("Enter a number: "))

# Calculate the power of 2
result = num ** 2

# Print the result
print(f"The result of {num} to the power of 2 is {result}")
# Calculate the power of 4
result_power_4 = num ** 4

# Print the result
print(f"The result of {num} to the power of 4 is {result_power_4}")